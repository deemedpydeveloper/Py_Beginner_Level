# Strings.

# anything between single quotes or double quotes will be treated as integers.
# print(len(12345)) ----> TypeError: Since len() holds only string length not an integer.
# indexing always start from zero --> this method of pulling out of specific character from string is called 'Subscript'.
print("Hello"[0])  # gives starting character of string.
print("Hello"[4])  # gives last character of string.