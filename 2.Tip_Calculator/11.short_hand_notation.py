# Short Hand Notation.

# Addition.
score = 10
score += 1
print(score)

# Subtraction.
score = 10
score -= 1
print(score)

# Multiplication.
score = 10
score *= 2
print(score)

# Division.
score = 10
score /= 2
print(score)

# Exponentiation.
score = 10
score **= 2
print(score)

# Floor Division.
score = 10
score //= 3
print(score) 