# Type Conversion.
# All possible conversions.

# string to integer conversion.
a = int('123')
print(type(a))
# string to float conversion.
a = float('123')
print(type(a))
# integer to string conversion.
a = str(123)
print(type(a))
# integer to float conversion.
a = float(123)
print(type(a))
# float to string conversion.
a = str(123.00)
print(type(a))
# float to int conversion.
a = int(123.123)
print(type(a))