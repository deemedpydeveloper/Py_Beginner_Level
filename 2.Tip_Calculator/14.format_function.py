# Format Function.

# a method to round floating point number to required decimal places.
# takes two arguments as input.
# one is "floating point value" and the other is "number of decimal places".
myFloat = 10.887555
print(format(myFloat, '.1f'))  # 1 decimal place.
print(format(myFloat, '.2f'))  # 2 decimal places.
print(format(myFloat, '.3f'))  # 3 decimal places.
print(format(myFloat, '.4f'))  # 4 decimal places.
print(format(myFloat, '.5f'))  # 5 decimal places.