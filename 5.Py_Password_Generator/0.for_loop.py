# For Loop.

## for ---> keyword used to describe function.
## in ---> also a keyword, which is used to tap into data structure.

fruits = ['Apple', 'Peach', 'Pear']
# fruit is a variable which takes one string from list for every iteration.
for fruit in fruits:
    print(fruit + " Pie")    # block of statements to be performed, when function holds to be True. 