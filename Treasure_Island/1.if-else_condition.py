# If-else Statements.

print("Welcome to the 'Roller Coaster'.")
# type conversion and storing in a variable.
height = int(input("What is your height in cm? "))
# checks if height is greater than or equals to 120.
if height >= 120:
    print("Enjoy your ride!")   # Indentation is provided to avoid 'IndentationError' in both the statements.
# if above condition evaluates to be 'False' then, this is executed.
else:
    print("Sorry!, You are not eligible.")