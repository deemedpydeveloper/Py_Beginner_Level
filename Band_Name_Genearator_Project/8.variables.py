# Python Variables.

# If prompt from the user need to be reused in the program, it is needed to be stored.
# This can be achieved by declaring and Initializing it to identifier called 'Variable'.
name = input("What is your name? ")
print(name)
# we can change the value of a variable.
name = 'Tej'
print(name)
# simplifying the code instead of input function
print(len(name))
# still simplifying the code instead of len function
length = len(name)
print(length)